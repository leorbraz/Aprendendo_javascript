function seuNome (name) {
    return 'Seu nome é ' + name;
}

seuNome ('Leonardo');

function verificarIdade(idade){{
        if (idade >= 18){
        console.log(seuNome('Leonardo') + ' e ja é maior de idade.');
        }
        else {
            console.log(seuNome('Leonardo') + ' e ainda é menor de idade.');
        }
    }
}

verificarIdade(25);


